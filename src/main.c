#include <stdio.h>
#include "znet_tcp.h"
#include "net/eth.h"

int main(void) {
    printf("Starting application...\n");
    uint8_t mock_frame[] = {
        // --- ETHERNET HEADER (14 Bytes) ---
        0x00, 0x1a, 0x2b, 0x3c, 0x4d, 0x5e, // Destination MAC
        0x5e, 0x4d, 0x3c, 0x2b, 0x1a, 0x00, // Source MAC
        0x08, 0x00,                         // EtherType (IPv4)
        
        // --- PAYLOAD (Starts at byte 14) ---
        // This is a dummy IPv4 header and some data to pad it to 60 bytes
        0x45, 0x00, 0x00, 0x2e, 0x00, 0x00, 0x00, 0x00, 
        0x40, 0x06, 0x00, 0x00, 0xc0, 0xa8, 0x01, 0x0a, 
        0xc0, 0xa8, 0x01, 0x14, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00
    };

    uint32_t frame_len = sizeof(mock_frame);
    eth_rcv(mock_frame, frame_len);
    
    if (ztcp_init() != 0) {
        fprintf(stderr, "Failed to initialize ZeroNet.\n");
        return 1;
    }

    test_mempool();
    printf("Application running with custom network stack.\n");
    return 0;
}